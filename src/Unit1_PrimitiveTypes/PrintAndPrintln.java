package Unit1_PrimitiveTypes;

/**
 *
 * @author maanderson
 * 
 * The difference between print() and println()
 * 
 */
public class PrintAndPrintln {
    
    public static void main(String[] args) {
        
        System.out.println("Hello World!"); //moves the cursor to the next line after printing
        
        System.out.print("Halo Dunia!");    //printed on the next line
        
        System.out.print("Bonjour!");       //printed on the same line as the preceding text
               
        System.out.println("Apa kabar!");   //printed on the same line as the preceding text
                                            //moves the cursor to the next line      
        System.out.print("Como esta?");     //printed on the next line
        
        
    }
}
