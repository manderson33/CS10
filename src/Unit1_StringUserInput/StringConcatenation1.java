public class StringConcatenation1 {
    
    public static void main(String[] args) {
        
        String word1 = "Elephant";
        
        String word2 = "Personality";
        
        // the '+' sign indicates a concatenation between strings
        String compWord = word1 + " " + word2;
        
        System.out.println(compWord);
             
    }
    
}
